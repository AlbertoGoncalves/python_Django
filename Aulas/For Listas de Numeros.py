# Listas de Numeros
lista_de_numeros = range(5)
for i in lista_de_numeros:
    print(i)

lista_de_numeros = range(5,10)
for i in lista_de_numeros:
    print(i)

lista_de_numeros = range(0,100,2) # renge com passo vai pilar de 2 em 2
for i in lista_de_numeros:
    print(i)

for i in range(0,100,2):
    print(i)

